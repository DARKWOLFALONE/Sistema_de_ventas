package modelo;

public class remisiones {
    private int id;
    private String Cliente;
    private String Destino;
    private String Transportado;
    private String Observaciín;
    private String Muestra;
    private String Fecha;

    public remisiones() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCliente() {
        return Cliente;
    }

    public void setCliente(String Cliente) {
        this.Cliente = Cliente;
    }

    public String getDestino() {
        return Destino;
    }

    public void setDestino(String Destino) {
        this.Destino = Destino;
    }

    public String getTransportado() {
        return Transportado;
    }

    public void setTransportado(String Transportado) {
        this.Transportado = Transportado;
    }

    public String getObservaciín() {
        return Observaciín;
    }

    public void setObservaciín(String Observaciín) {
        this.Observaciín = Observaciín;
    }

    public String getMuestra() {
        return Muestra;
    }

    public void setMuestra(String Muestra) {
        this.Muestra = Muestra;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }
    
    
}
