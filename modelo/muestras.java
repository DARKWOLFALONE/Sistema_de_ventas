package modelo;

public class muestras {
    private int id;
    private String Cliente;
    private String Marca;
    private String Tipo_De_Prenda;
    private int Referencia;
    private int Tela;
    private double Peso;
    private int Número_De_Unidades;
    private String Color_de_tira;
    private String Muestra_en_foto;
    private String MF;
    private String Observación;
    private String Fecha;

    public muestras() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCliente() {
        return Cliente;
    }

    public void setCliente(String Cliente) {
        this.Cliente = Cliente;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getTipo_De_Prenda() {
        return Tipo_De_Prenda;
    }

    public void setTipo_De_Prenda(String Tipo_De_Prenda) {
        this.Tipo_De_Prenda = Tipo_De_Prenda;
    }

    public int getReferencia() {
        return Referencia;
    }

    public void setReferencia(int Referencia) {
        this.Referencia = Referencia;
    }

    public int getTela() {
        return Tela;
    }

    public void setTela(int Tela) {
        this.Tela = Tela;
    }

    public double getPeso() {
        return Peso;
    }

    public void setPeso(double Peso) {
        this.Peso = Peso;
    }

    public int getNúmero_De_Unidades() {
        return Número_De_Unidades;
    }

    public void setNúmero_De_Unidades(int Número_De_Unidades) {
        this.Número_De_Unidades = Número_De_Unidades;
    }

    public String getColor_de_tira() {
        return Color_de_tira;
    }

    public void setColor_de_tira(String Color_de_tira) {
        this.Color_de_tira = Color_de_tira;
    }

    public String getMuestra_en_foto() {
        return Muestra_en_foto;
    }

    public void setMuestra_en_foto(String Muestra_en_foto) {
        this.Muestra_en_foto = Muestra_en_foto;
    }

    public String getMF() {
        return MF;
    }

    public void setMF(String MF) {
        this.MF = MF;
    }

    public String getObservación() {
        return Observación;
    }

    public void setObservación(String Observación) {
        this.Observación = Observación;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }
    
}
