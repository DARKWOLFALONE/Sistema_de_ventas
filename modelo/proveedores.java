package modelo;

public class proveedores {
        private int id;
        private String Nombre;
        private String Tipo_De_Identificación;
        private int Número_De_Indentificaciión;
        private String Nombre_De_Contacto;
        private int Télefono;
        private String Direcciíon;
        private String Correo;
        private String Estado;
        private String Observación;
        private String Fecha;

    public proveedores() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getTipo_De_Identificación() {
        return Tipo_De_Identificación;
    }

    public void setTipo_De_Identificación(String Tipo_De_Identificación) {
        this.Tipo_De_Identificación = Tipo_De_Identificación;
    }

    public int getNúmero_De_Indentificaciión() {
        return Número_De_Indentificaciión;
    }

    public void setNúmero_De_Indentificaciión(int Número_De_Indentificaciión) {
        this.Número_De_Indentificaciión = Número_De_Indentificaciión;
    }

    public String getNombre_De_Contacto() {
        return Nombre_De_Contacto;
    }

    public void setNombre_De_Contacto(String Nombre_De_Contacto) {
        this.Nombre_De_Contacto = Nombre_De_Contacto;
    }

    public int getTélefono() {
        return Télefono;
    }

    public void setTélefono(int Télefono) {
        this.Télefono = Télefono;
    }

    public String getDirecciíon() {
        return Direcciíon;
    }

    public void setDirecciíon(String Direcciíon) {
        this.Direcciíon = Direcciíon;
    }

    public String getCorreo() {
        return Correo;
    }

    public void setCorreo(String Correo) {
        this.Correo = Correo;
    }

    public String getEstado() {
        return Estado;
    }

    public void setEstado(String Estado) {
        this.Estado = Estado;
    }

    public String getObservación() {
        return Observación;
    }

    public void setObservación(String Observación) {
        this.Observación = Observación;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }
        
    
}
